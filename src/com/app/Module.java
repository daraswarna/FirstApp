package com.app;

public class Module {

	private int modId;
	private String modName;
	public Module() {
		super();
	}
	public int getModId() {
		return modId;
	}
	public void setModId(int modId) {
		this.modId = modId;
	}
	public String getModName() {
		return modName;
	}
	public void setModName(String modName) {
		this.modName = modName;
	}
	
}
